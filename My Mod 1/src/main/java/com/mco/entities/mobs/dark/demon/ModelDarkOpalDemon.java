	@Override
	public void setRotationAngles(float limbSwing, float limbSwingAmount, float ageInTicks, float netHeadYaw, float headPitch, float scaleFactor, Entity entityIn) 
	{
		resetToDefaultPose();
	
        this.HeadCubeA.rotateAngleY = netHeadYaw * 0.017453292F;
        this.HeadCubeA.rotateAngleZ = headPitch * -0.017453292F;
            
		EntityDarkOpalDemon darkDemon = (EntityDarkOpalDemon) entityIn;

//		limbSwing = entityIn.ticksExisted;
//		limbSwingAmount = 0.5F;

		float globalSpeed = 1F;
		float globalHeight = 1F;
		float globalDegree = 1F;
		
		AdvancedModelRenderer[] RIGHT_THIGHS = {RKnee, RThighA, RThighB, RThighC, RThighD};
		AdvancedModelRenderer[] RIGHT_QUADS = {RQuadB, RQuadA};
		
		AdvancedModelRenderer[] LEFT_THIGHS = {LKnee, LThighA, LThighB, LThighC, LThighD};
		AdvancedModelRenderer[] LEFT_QUADS = {LQuadA, LQuadB};

		AdvancedModelRenderer[] LEFT_ARM = {LShoulderA, LBicepA, LForearmA};
		
		AdvancedModelRenderer[] RIGHT_WING = {RWingStrutA, RWingStrutB, RWingStrutC, RWingStrutD};
		AdvancedModelRenderer[] LEFT_WING = {LWingStrutA, LWingStrutB, LWingStrutC, LWingStrutD};
		
		bob(TorsoC, .4F * globalSpeed, 1 * globalHeight, false, limbSwing, limbSwingAmount);
		bob(TorsoDChest, .4F * globalSpeed, 1 * globalHeight, false, limbSwing, limbSwingAmount);

		chainFlap(RIGHT_QUADS, .2F * globalSpeed, .5F * globalDegree, 0, limbSwing, limbSwingAmount);
		chainFlap(RIGHT_THIGHS, .2F * globalSpeed, -.1F * globalDegree, .2, limbSwing, limbSwingAmount);
		
		chainFlap(LEFT_QUADS, .2F * globalSpeed, -.5F * globalDegree, 0, limbSwing, limbSwingAmount);
		chainFlap(LEFT_THIGHS, .2F * globalSpeed, .1F * globalDegree, 0, limbSwing, limbSwingAmount);
		
		flap(RShoulderA, .2F * globalSpeed, .2F * globalDegree, true, 0, 0, limbSwing, limbSwingAmount);
		flap(RBicepA, .2F * globalSpeed, .2F * globalDegree, false, 0, 0, limbSwing, limbSwingAmount);
		flap(RForearmA, .2F * globalSpeed, .2F * globalDegree, false, 0, 0, limbSwing, limbSwingAmount);
		chainFlap(LEFT_ARM, .2F * globalSpeed, .2F * globalDegree, 0, limbSwing, limbSwingAmount);

		chainSwing(RIGHT_WING, .2F * globalSpeed, -.3F * globalDegree, 1, limbSwing, limbSwingAmount);
		chainFlap(RIGHT_WING, .2F * globalSpeed, .1F * globalDegree, 0.1F, limbSwing, limbSwingAmount);
		
		chainSwing(LEFT_WING, .2F * globalSpeed, -.3F * globalDegree, 1, limbSwing, limbSwingAmount);
		chainFlap(LEFT_WING, .2F * globalSpeed, .1F * globalDegree, 0.1F, limbSwing, limbSwingAmount);
		
		if(darkDemon.getAnimation() == darkDemon.ANIMATION_JUMP)
		{
			if(darkDemon.getAnimationTick() > 20 && darkDemon.getAnimationTick() < 60) 
			{
				chainSwing(RIGHT_WING, 1F * globalSpeed, -.4F * globalDegree, 2, darkDemon.ticksExisted, 0.5F);
				chainSwing(LEFT_WING, 1F * globalSpeed, .4F * globalDegree, 2, darkDemon.ticksExisted, 0.5F);
			}
			else if(darkDemon.getAnimationTick() > 60 && darkDemon.getAnimationTick() < 85)
			{
				chainFlap(RIGHT_WING, 3F * globalSpeed, .2F * globalDegree, 0.1F, darkDemon.ticksExisted, 0.5F);
				chainFlap(LEFT_WING, 3F * globalSpeed, .2F * globalDegree, 0.1F, darkDemon.ticksExisted, 0.5F);
			}
		}
	}
